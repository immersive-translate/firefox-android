package org.mozilla.fenix.immersive_transalte.bean;

public class ResultData<T> {
    int code;
    T data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }
}
